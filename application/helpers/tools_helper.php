<?php

function convertToSeo($text){
    $turkce = array ("ı", "İ", "ç", "Ç", "Ü", "ü", "Ö", "ö", "ş", "Ş", "ğ", "Ğ","\"","?","!","*"," ","|","_","=",",",".","(",")","[","]","{","}","%","&","#","$");
    $convert = array ("i", "i", "c", "c", "u", "u", "o", "o", "s", "s", "g", "g","-","-","-","-","-","-","-","-","-","-","-","-","-","-","-","-","-","-","-","-");
    return  strtolower(str_replace($turkce, $convert, $text));
}
